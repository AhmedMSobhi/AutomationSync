let stepCounter = 0;

export function resetFlowSteps(): void {
  stepCounter = 0;
}

export function logFlowStep(title: string, detail?: string | Record<string, unknown>): void {
  stepCounter += 1;
  console.log(`\nв”Њв”Ђ STEP ${stepCounter}: ${title} в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ`);
  if (detail !== undefined) {
    if (typeof detail === 'string') {
      console.log(`в”‚  ${detail}`);
    } else {
      console.log(`в”‚  ${JSON.stringify(detail, null, 2).replace(/\n/g, '\nв”‚  ')}`);
    }
  }
  console.log('в””в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ');
}

